piano_key = ["a","b","c","d","e","f","g"]
print(piano_key[2:5])
# ['c', 'd', 'e']
print(piano_key[2:])
# ['c', 'd', 'e', 'f', 'g']
print(piano_key[:5])
# ['a', 'b', 'c', 'd', 'e']
print(piano_key[2:5:2])
# ['c', 'e']
print(piano_key[::2])
# ['a', 'c', 'e', 'g']
print(piano_key[::-1])
# ['g', 'f', 'e', 'd', 'c', 'b', 'a']
